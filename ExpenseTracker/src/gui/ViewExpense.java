    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            javax.swing.table.DefaultTableModel dtm=
                 (javax.swing.table.DefaultTableModel)table1.getModel();
         int rc=dtm.getRowCount();
         while(rc--!=0){
             dtm.removeRow(0);
                 }
        java.sql.Date dt1=new java.sql.Date(d1.getDate().getTime());
         java.sql.Date dt2=new java.sql.Date(d2.getDate().getTime());
         
        ResultSet rs= db.dbcode.s.executeQuery("select*from expense where"
                 + " E_date>='"+dt1+"' and E_date<='"+dt2+"' order by E_date");
        int total=0;
         while(rs.next()){
             int t=rs.getInt("amount");
             total+=t;
             Object o[]={rs.getDate("E_date"),rs.getString("categoryy"),rs.getInt("amount")};
         dtm.addRow(o);
         
         }
         total1.setText(total+"");
         
        }
        catch(Exception ex){
        JOptionPane.showMessageDialog(null, "No Date Entered!!!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

